
public class Calculator {
	
	Calculator(){}
	
	void plus(int a, int b){
		int c = a + b;
		System.out.print(c);
	}
	
	void minus(int a, int b){
		int c = a - b;
		System.out.print(c);
	}
	
	void mult(int a, int b){
		int c = a*b;
		System.out.print(c);
	}
	
	void divide(int a, int b){
		int c = a/b;
		System.out.print(c);
	}
}
